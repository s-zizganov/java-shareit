package ru.practicum.shareit.booking;

/**
 * Перечисление, представляющее возможные статусы бронирования в системе.
 * Используется для отслеживания жизненного цикла бронирования от создания до завершения.
 */
public enum BookingStatus {
    /**
     * Бронирование создано и ожидает подтверждения от владельца вещи.
     * Это начальный статус для всех новых бронирований.
     */
    WAITING,
    /**
     * Бронирование подтверждено владельцем вещи.
     * Означает, что запрос на бронирование принят и пользователь может использовать вещь
     * в указанный период времени.
     */
    APPROVED,
    /**
     * Бронирование отклонено владельцем вещи.
     */
    REJECTED,
    /**
     * Бронирование отменено пользователем.
     */
    CANCELED
}